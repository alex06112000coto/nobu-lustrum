// DOM References
const daySelector = document.getElementById("program-day-selector");
const dayTitle = document.getElementById("program-day-title");
const dayDate = document.getElementById("program-day-date");
const timelineContainer = document.getElementById("program-timeline-container");
const toggleProgramBtn = document.getElementById("toggle-program-tab-btn");
const toggleInfoBtn = document.getElementById("toggle-info-tab-btn");
const subviewProgram = document.getElementById("subview-program");
const subviewInfo = document.getElementById("subview-info");

// Render a selected day's timeline
function renderDay(dayId) {
  const data = itineraryData[dayId];
  if (!data) return;

  // Set card titles safely
  dayTitle.textContent = data.title;
  dayDate.textContent = data.date;

  // Clear timeline container
  timelineContainer.replaceChildren();

  // Create timeline elements dynamically
  data.events.forEach(event => {
    const eventDiv = document.createElement("div");
    eventDiv.className = "timeline-event";

    const dot = document.createElement("div");
    dot.className = "timeline-dot";
    eventDiv.appendChild(dot);

    const time = document.createElement("div");
    time.className = "timeline-time";
    time.textContent = event.time;
    eventDiv.appendChild(time);

    const title = document.createElement("div");
    title.className = "timeline-title";
    title.textContent = event.title;
    eventDiv.appendChild(title);

    const desc = document.createElement("div");
    desc.className = "timeline-desc";
    desc.textContent = event.desc;
    eventDiv.appendChild(desc);

    timelineContainer.appendChild(eventDiv);
  });
}

// Set up UI Event Listeners
export function init() {
  console.log("Initializing travel program view...");
  
  // Day Selector Change Listener
  daySelector.addEventListener("change", (e) => {
    const selectedDay = e.target.value;
    
    // Add micro-animation effect during page redraw
    const card = document.getElementById("program-day-content-card");
    card.style.animation = 'none';
    void card.offsetWidth; // trigger reflow
    card.style.animation = 'fadeIn 0.25s ease';
    
    renderDay(selectedDay);
  });

  // Toggle buttons between Program timeline and Info packlist
  toggleProgramBtn.addEventListener("click", () => {
    toggleProgramBtn.className = "btn-primary";
    toggleInfoBtn.className = "btn-secondary";
    toggleInfoBtn.style.background = "transparent";
    toggleInfoBtn.style.border = "none";
    
    subviewProgram.style.display = "block";
    subviewInfo.style.display = "none";
  });

  toggleInfoBtn.addEventListener("click", () => {
    toggleInfoBtn.className = "btn-primary";
    toggleProgramBtn.className = "btn-secondary";
    toggleProgramBtn.style.background = "transparent";
    toggleProgramBtn.style.border = "none";
    
    subviewProgram.style.display = "none";
    subviewInfo.style.display = "block";
  });

  // Initial render of first day
  renderDay("1");
}
